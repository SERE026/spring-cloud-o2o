package com.icloud.dto;

import com.icloud.model.MerchantsSupplier;

/**
 * 商户帐户详情Dto
 * 
 * @author luoqw 2016-9-7上午10:10:57
 */
public class SupplierDetailDto extends BaseDto {
 
	private MerchantsSupplier merchants;
	
	
	
	public SupplierDetailDto(String resultType, String resultCode,
			MerchantsSupplier merchants) {
		super();
		this.resultType = resultType;
		this.resultCode = resultCode;
		this.merchants = merchants;
	}

	public SupplierDetailDto(String resultType, String resultCode,
			String errorMsg) {
		super();
		this.resultType = resultType;
		this.resultCode = resultCode;
		this.errorMsg = errorMsg;
	}

	public String getResultType() {
		return resultType;
	}

	public void setResultType(String resultType) {
		this.resultType = resultType;
	}

	public String getResultCode() {
		return resultCode;
	}

	public void setResultCode(String resultCode) {
		this.resultCode = resultCode;
	}

	public String getErrorMsg() {
		return errorMsg;
	}

	public void setErrorMsg(String errorMsg) {
		this.errorMsg = errorMsg;
	}

	public MerchantsSupplier getMerchants() {
		return merchants;
	}

	public void setMerchants(MerchantsSupplier merchants) {
		this.merchants = merchants;
	}

	

}
