package com.merchant.form;

public class MerchantsMpForm {
	private int offset = 0;
	private int pageSize = 10;
	private MerchantsMpConditionsQuery queryConditions;

	public int getOffset() {
		return offset;
	}

	public void setOffset(int offset) {
		this.offset = offset;
	}

	public int getPageSize() {
		return pageSize;
	}

	public void setPageSize(int pageSize) {
		this.pageSize = pageSize;
	}

	public MerchantsMpConditionsQuery getQueryConditions() {
		return queryConditions;
	}

	public void setQueryConditions(MerchantsMpConditionsQuery queryConditions) {
		this.queryConditions = queryConditions;
	}

}
