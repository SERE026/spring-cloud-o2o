package com.icloud.dto;

public class TakeTokenDto {

	private String resultType;
	private String resultCode;
	private String errorMsg;
	private String accessToken;
	private String expiresIn;

	public TakeTokenDto(String resultType, String resultCode, String accessToken, String expiresIn) {
		super();
		this.resultType = resultType;
		this.resultCode = resultCode;
		this.accessToken = accessToken;
		this.expiresIn = expiresIn;
	}

	public TakeTokenDto(String resultType, String resultCode, String errorMsg) {
		super();
		this.resultType = resultType;
		this.resultCode = resultCode;
		this.errorMsg = errorMsg;
	}

	public String getResultType() {
		return resultType;
	}

	public void setResultType(String resultType) {
		this.resultType = resultType;
	}

	public String getResultCode() {
		return resultCode;
	}

	public void setResultCode(String resultCode) {
		this.resultCode = resultCode;
	}

	public String getErrorMsg() {
		return errorMsg;
	}

	public void setErrorMsg(String errorMsg) {
		this.errorMsg = errorMsg;
	}

	public String getAccessToken() {
		return accessToken;
	}

	public void setAccessToken(String accessToken) {
		this.accessToken = accessToken;
	}

	public String getExpiresIn() {
		return expiresIn;
	}

	public void setExpiresIn(String expiresIn) {
		this.expiresIn = expiresIn;
	}

}
